#include <stdio.h>

/**
 * main: block
 * discription: print alphabet to lowwercase
 * and then to uppercase followed by newline
 * Return: Always 0 (Success)
 */
int main(void)
{
	int ch;

	for (ch = 'a'; ch <= 'z'; ch++)
		putchar(ch);
	for (ch = 'A'; ch <= 'Z'; ch++)
		putchar(ch);
	putchar('\n');
	return (0);
}
